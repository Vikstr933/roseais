import Anthropic from '@anthropic-ai/sdk';
import { Logger } from '../utils/Logger';

export interface AIGenerationRequest {
  prompt: string;
  componentName: string;
  features: string[];
  styling?: {
    animations: boolean;
    theme: string;
  };
  context?: string;
}

export interface AIGenerationResponse {
  success: boolean;
  code: string;
  dependencies: string[];
  error?: string;
}

export class AICodeGenerator {
  private anthropic: Anthropic;
  private logger: Logger;

  constructor() {
    this.anthropic = new Anthropic({
      apiKey: process.env.ANTHROPIC_API_KEY,
    });
    this.logger = new Logger(process.cwd());
    this.logger.initialize().catch(console.error);
  }

  async generateComponent(request: AIGenerationRequest): Promise<AIGenerationResponse> {
    try {
      this.logger.info('AICodeGenerator', 'Generating component with AI', {
        componentName: request.componentName,
        features: request.features,
      });

      const systemPrompt = this.buildSystemPrompt(request);
      const userPrompt = this.buildUserPrompt(request);

      const response = await this.anthropic.messages.create({
        model: 'claude-3-5-sonnet-20241022',
        max_tokens: 4000,
        temperature: 0.7,
        system: systemPrompt,
        messages: [
          {
            role: 'user',
            content: userPrompt,
          },
        ],
      });

      const generatedCode = response.content[0]?.type === 'text' 
        ? response.content[0].text 
        : '';

      if (!generatedCode) {
        throw new Error('No code generated by AI');
      }

      // Extract dependencies from the generated code
      const dependencies = this.extractDependencies(generatedCode);

      this.logger.info('AICodeGenerator', 'Component generated successfully', {
        codeLength: generatedCode.length,
        dependencies,
      });

      return {
        success: true,
        code: generatedCode,
        dependencies,
      };
    } catch (error) {
      const errorMessage = error instanceof Error ? error.message : 'Unknown error';
      this.logger.error('AICodeGenerator', 'Failed to generate component', {
        error: errorMessage,
      });

      return {
        success: false,
        code: '',
        dependencies: [],
        error: errorMessage,
      };
    }
  }

  private buildSystemPrompt(request: AIGenerationRequest): string {
    return `You are an expert React developer specializing in creating modern, functional web applications. 

Your task is to generate a complete, working React component based on the user's requirements.

REQUIREMENTS:
- Use React 18+ with TypeScript
- Use modern React patterns (hooks, functional components)
- Include proper TypeScript types and interfaces
- Use Tailwind CSS for styling
- Make the component fully functional and interactive
- Include proper error handling
- Use semantic HTML elements
- Make it responsive and accessible
- Include realistic data and functionality

COMPONENT SPECIFICATIONS:
- Component Name: ${request.componentName}
- Features: ${request.features.join(', ')}
- Theme: ${request.styling?.theme || 'light'}
- Animations: ${request.styling?.animations ? 'Include smooth animations' : 'No animations needed'}

OUTPUT FORMAT:
Return ONLY the complete React component code. Do not include explanations, comments about the code, or any other text. The code should be ready to use immediately.

The component should be a complete, functional implementation that matches the user's requirements exactly.`;
  }

  private buildUserPrompt(request: AIGenerationRequest): string {
    let prompt = `Create a React component called "${request.componentName}" with the following requirements:\n\n`;
    
    prompt += `DESCRIPTION: ${request.prompt}\n\n`;
    
    if (request.features.length > 0) {
      prompt += `REQUIRED FEATURES:\n`;
      request.features.forEach(feature => {
        prompt += `- ${feature}\n`;
      });
      prompt += '\n';
    }

    if (request.context) {
      prompt += `ADDITIONAL CONTEXT: ${request.context}\n\n`;
    }

    prompt += `Please create a fully functional, modern React component that implements all the requested features. The component should be production-ready with proper TypeScript types, error handling, and responsive design.`;

    return prompt;
  }

  private extractDependencies(code: string): string[] {
    const dependencies: string[] = [];
    
    // Common dependencies to check for
    const dependencyMap: Record<string, string> = {
      'useState': 'react',
      'useEffect': 'react',
      'useCallback': 'react',
      'useMemo': 'react',
      'useRef': 'react',
      'framer-motion': 'framer-motion',
      'react-router': 'react-router-dom',
      'axios': 'axios',
      'date-fns': 'date-fns',
      'lodash': 'lodash',
      'uuid': 'uuid',
      'react-hook-form': 'react-hook-form',
      'zod': 'zod',
      'recharts': 'recharts',
      'lucide-react': 'lucide-react',
      'clsx': 'clsx',
      'tailwind-merge': 'tailwind-merge',
    };

    // Check for imports and usage
    for (const [importName, packageName] of Object.entries(dependencyMap)) {
      if (code.includes(importName) && !dependencies.includes(packageName)) {
        dependencies.push(packageName);
      }
    }

    // Always include React dependencies
    if (!dependencies.includes('react')) {
      dependencies.push('react');
    }
    if (!dependencies.includes('react-dom')) {
      dependencies.push('react-dom');
    }

    return dependencies;
  }

  async generateHooks(componentName: string, features: string[]): Promise<string> {
    try {
      const systemPrompt = `You are an expert React developer. Generate custom hooks for a React component.

Create TypeScript custom hooks that would be useful for a "${componentName}" component with these features: ${features.join(', ')}.

Return ONLY the hook code, no explanations.`;

      const userPrompt = `Generate useful custom hooks for the ${componentName} component. Focus on hooks that would be commonly used with these features: ${features.join(', ')}.`;

      const response = await this.anthropic.messages.create({
        model: 'claude-3-5-sonnet-20241022',
        max_tokens: 2000,
        temperature: 0.7,
        system: systemPrompt,
        messages: [{ role: 'user', content: userPrompt }],
      });

      return response.content[0]?.type === 'text' ? response.content[0].text : '';
    } catch (error) {
      this.logger.error('AICodeGenerator', 'Failed to generate hooks', { error });
      return '';
    }
  }

  async generateTypes(componentName: string, features: string[]): Promise<string> {
    try {
      const systemPrompt = `You are an expert TypeScript developer. Generate TypeScript type definitions for a React component.

Create comprehensive TypeScript interfaces and types for a "${componentName}" component with these features: ${features.join(', ')}.

Return ONLY the TypeScript type definitions, no explanations.`;

      const userPrompt = `Generate TypeScript types and interfaces for the ${componentName} component. Include all necessary types for the features: ${features.join(', ')}.`;

      const response = await this.anthropic.messages.create({
        model: 'claude-3-5-sonnet-20241022',
        max_tokens: 1500,
        temperature: 0.7,
        system: systemPrompt,
        messages: [{ role: 'user', content: userPrompt }],
      });

      return response.content[0]?.type === 'text' ? response.content[0].text : '';
    } catch (error) {
      this.logger.error('AICodeGenerator', 'Failed to generate types', { error });
      return '';
    }
  }
}

// Export singleton instance
export const aiCodeGenerator = new AICodeGenerator();
