export interface AIGenerationRequest {
  prompt: string;
  componentName: string;
  features: string[];
  styling?: {
    animations: boolean;
    theme: string;
  };
  context?: string;
  modelPreference?: 'speed' | 'quality' | 'cost';
  provider?: 'anthropic' | 'openai' | 'gemini' | 'auto';
}

export interface AIGenerationResponse {
  success: boolean;
  code?: string; // Main component code (for backwards compatibility)
  files?: { path: string; content: string }[]; // Multi-file support
  dependencies: string[];
  error?: string;
  warning?: string; // For non-fatal issues like parsing fallbacks
  parseWarning?: string; // Specific warning about file parsing
  metadata?: {
    provider: string;
    model: string;
    responseTime: number;
    tokens: number;
    cost: number;
    parsingMethod?: 'json' | 'markdown' | 'fallback';
  };
}

