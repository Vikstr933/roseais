  private logger: Logger;
  private limiter: RateLimiter;

  constructor() {
    this.anthropic = new Anthropic({
      apiKey: process.env.ANTHROPIC_API_KEY,
    });
    this.logger = new Logger(process.cwd());
    this.logger.initialize().catch(console.error);
    this.limiter = new RateLimiter(5);
  }

  async generateComponent(request: AIGenerationRequest): Promise<AIGenerationResponse> {
    try {
      console.log('🎨 AICodeGenerator: Starting component generation');
      console.log('📝 AICodeGenerator: Request:', {
        componentName: request.componentName,
        features: request.features,
        promptLength: request.prompt.length
      });

      this.logger.info('AICodeGenerator', 'Generating component with multi-model AI', {
        componentName: request.componentName,
        features: request.features,
        provider: request.provider || 'auto',
        preference: request.modelPreference || 'quality',
      });

      console.log('🏗️ AICodeGenerator: Building prompts');
      const systemPrompt = this.buildSystemPromptMultiFile(request);
      const userPrompt = this.buildUserPromptMultiFile(request);
      console.log('📊 AICodeGenerator: Prompt sizes:', {
        system: systemPrompt.length,
        user: userPrompt.length
      });

      // Use multi-model AI service
      const aiRequest: AIRequest = {
        prompt: userPrompt,
        systemPrompt,
        maxTokens: 8000,
        temperature: 0.7,
        useCase: 'code_generation',
        priority: request.modelPreference || 'quality'
      };

      console.log('🤖 AICodeGenerator: Calling multiModelAI.generate()...');
      const startTime = Date.now();
      const response = await multiModelAI.generate(aiRequest);
      const elapsed = Date.now() - startTime;
      console.log(`✅ AICodeGenerator: multiModelAI.generate() completed in ${elapsed}ms`);

      if (!response.content) {
        throw new Error('No code generated by AI');
      }

      // Parse the multi-file JSON response
      const parseResult = this.parseMultiFileResponse(response.content);

      if (parseResult.files.length === 0) {
        throw new Error('AI generated no files');
      }

      // Extract all dependencies from all files
      const dependencies = this.extractDependenciesFromFiles(parseResult.files);

      this.logger.info('AICodeGenerator', 'Multi-file component generated successfully', {
        filesCount: parseResult.files.length,
        dependencies,
        provider: response.provider,
        model: response.model,
        responseTime: response.responseTime,
        cost: response.usage.cost,
        parsingMethod: parseResult.method,
      });

      // Ensure all required configuration files are present
      const completeFiles = this.ensureRequiredFiles(parseResult.files, dependencies, request.componentName);

      const result: AIGenerationResponse = {
        success: true,
        files: completeFiles,
        code: completeFiles.find(f => f.path === 'src/App.tsx')?.content || '', // Backwards compatibility
        dependencies,
        metadata: {
          provider: response.provider,
          model: response.model,
          responseTime: response.responseTime,
          tokens: response.usage.totalTokens,
          cost: response.usage.cost,
          parsingMethod: parseResult.method,
        },
      };

      // Add warning if fallback parsing was used
      if (parseResult.warning) {
        result.parseWarning = parseResult.warning;
        result.warning = parseResult.warning;
      }

      return result;
    } catch (error) {
      const errorMessage = error instanceof Error ? error.message : 'Unknown error';
      this.logger.error('AICodeGenerator', 'Failed to generate component', {
        error: errorMessage,
      });

      return {
        success: false,
        code: '',
        files: [],
        dependencies: [],
        error: errorMessage,
      };
    }
  }

