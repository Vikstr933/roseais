  private parseMultiFileResponse(content: string): {
    files: { path: string; content: string }[];
    method: 'json' | 'markdown' | 'fallback';
    warning?: string;
  } {
    try {
      // Strategy 1: Try to parse as JSON
      let jsonContent = content.trim();

      // Remove ```json and ``` markers
      jsonContent = jsonContent.replace(/^```json\s*/i, '').replace(/```\s*$/, '');

      // Try to extract JSON array if wrapped in other text
      const jsonMatch = jsonContent.match(/\[\s*\{[\s\S]*\}\s*\]/);
      if (jsonMatch) {
        jsonContent = jsonMatch[0];
      }

      const files = JSON.parse(jsonContent);

      if (!Array.isArray(files)) {
        throw new Error('Response is not an array');
      }

      // Validate each file has path and content
      const validFiles = files.filter(f => f.path && f.content);

      if (validFiles.length === 0) {
        throw new Error('No valid files in JSON response');
      }

      this.logger.info('AICodeGenerator', `Successfully parsed ${validFiles.length} files from JSON`);
      return {
        files: validFiles,
        method: 'json'
      };
    } catch (error) {
      this.logger.warning('AICodeGenerator', 'JSON parsing failed, trying markdown extraction', {
        error: error instanceof Error ? error.message : 'Unknown error'
      });

      // Strategy 2: Try to extract files from markdown code blocks
      const markdownFiles = this.extractFilesFromMarkdown(content);
      if (markdownFiles.length > 0) {
        this.logger.info('AICodeGenerator', `Extracted ${markdownFiles.length} files from markdown`);
        return {
          files: markdownFiles,
          method: 'markdown',
          warning: `⚠️ File parsing used markdown extraction fallback. The AI response was not in the expected JSON format. Generated ${markdownFiles.length} file(s).`
        };
      }

      // Strategy 3: Last resort - return as single file with imports removed
      this.logger.error('AICodeGenerator', '❌ All parsing strategies failed, falling back to single file');

      // Remove local imports from the content
      const cleanedContent = this.stripLocalImports(content);

      return {
        files: [{
          path: 'src/App.tsx',
          content: cleanedContent
        }],
        method: 'fallback',
        warning: `⚠️ CRITICAL: Failed to parse multi-file structure. The AI response could not be properly parsed as JSON or markdown.

All code has been combined into a single file (src/App.tsx) with local imports removed. This may result in a non-functional application.

Suggestions to fix:
1. Try regenerating with a more specific prompt
2. Ask for "a complete multi-file React application with separate component files"
3. Specify the exact file structure you want
4. Consider using orchestration mode for complex applications`
      };
    }
  }

  private extractFilesFromMarkdown(content: string): { path: string; content: string }[] {
    const files: { path: string; content: string }[] = [];

    // Match patterns like: **src/App.tsx** or **index.html** or ### src/components/Button.tsx
    // followed by code blocks
    // Updated to match both src/ files AND root files (index.html, package.json, etc.)
    const filePattern = /(?:\*\*|###)\s*([^\s*\n]+\.(?:tsx?|jsx?|css|json|html|js|config\.ts|config\.js))\s*(?:\*\*)?[\s\n]*```(?:typescript|javascript|tsx|jsx|css|json|html)?\s*([\s\S]*?)```/gi;

    let match;
    while ((match = filePattern.exec(content)) !== null) {
      const path = match[1].trim();
      const fileContent = match[2].trim();

      if (path && fileContent) {
        files.push({ path, content: fileContent });
      }
    }

    this.logger.info('AICodeGenerator', `extractFilesFromMarkdown found ${files.length} files`, {
      filePaths: files.map(f => f.path)
    });

    return files;
  }

  private stripLocalImports(code: string): string {
    const lines = code.split('\n');
    const filteredLines = lines.filter(line => {
      const trimmed = line.trim();
      // Remove lines with local imports
      if (trimmed.startsWith('import') && /from\s+['"]\.\.?\//.test(trimmed)) {
        return false;
      }
      return true;
    });
    return filteredLines.join('\n');
  }

  private extractDependenciesFromFiles(files: { path: string; content: string }[]): string[] {
    const allDeps = new Set<string>();

    files.forEach(file => {
      const deps = this.extractDependencies(file.content);
      deps.forEach(dep => allDeps.add(dep));
    });

    return Array.from(allDeps);
  }

  private extractDependencies(code: string): string[] {
    const dependencies: string[] = [];

    // Common dependencies to check for
    const dependencyMap: Record<string, string> = {
      'useState': 'react',
      'useEffect': 'react',
      'useCallback': 'react',
      'useMemo': 'react',
      'useRef': 'react',
      'framer-motion': 'framer-motion',
      'react-router': 'react-router-dom',
      'axios': 'axios',
      'date-fns': 'date-fns',
      'lodash': 'lodash',
      'uuid': 'uuid',
      'react-hook-form': 'react-hook-form',
      'zod': 'zod',
      'recharts': 'recharts',
      'lucide-react': 'lucide-react',
      'clsx': 'clsx',
      'tailwind-merge': 'tailwind-merge',
    };

    // Check for imports and usage
    for (const [importName, packageName] of Object.entries(dependencyMap)) {
      if (code.includes(importName) && !dependencies.includes(packageName)) {
        dependencies.push(packageName);
      }
    }

    // Always include React dependencies
    if (!dependencies.includes('react')) {
      dependencies.push('react');
    }
    if (!dependencies.includes('react-dom')) {
      dependencies.push('react-dom');
    }

    return dependencies;
  }

